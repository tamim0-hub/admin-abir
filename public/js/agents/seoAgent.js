class SEOAgent {
    constructor() {
        this.name = 'SEO Agent';
        this.icon = '🔍';
        this.router = getRouter();
        this.tracker = getTokenTracker();
        this.perf = getPerfTracker();
    }

    async analyzeWebsite(url) {
        const startTime = Date.now();
        
        const messages = [
            { role: 'system', content: `You are an SEO Agent. Analyze this website and give detailed SEO report.
            Check:
            1. Title tags & meta descriptions
            2. Heading structure (H1, H2, H3)
            3. Content quality & readability
            4. Image alt texts
            5. Internal & external links
            6. Page speed (estimate)
            7. Mobile friendliness (estimate)
            8. Schema markup
            9. Keyword density
            10. Competitor comparison
            
            Give score 0-100 and actionable recommendations.
            Return as structured JSON.` },
            { role: 'user', content: `Website URL: ${url}` }
        ];

        const result = await this.router.chat('seoAgent', messages, { temperature: 0.3 });
        const time = (Date.now() - startTime) / 1000;

        if (result.success) {
            this.tracker.track('seoAgent', result.model, result.tokens);
            this.perf.track('seoAgent', result.model, true, time, result.tokens);
            try {
                return JSON.parse(result.content);
            } catch {
                return { error: 'Parse failed', raw: result.content };
            }
        } else {
            this.perf.track('seoAgent', 'unknown', false, time, 0);
            return { error: result.error };
        }
    }

    generatePDFReport(data) {
        // PDF রিপোর্ট জেনারেশন (সিম্পল টেক্সট ফরম্যাটে)
        let report = '═══════════════════════════════════════\n';
        report += '     📊 SEO REPORT - dropship.com\n';
        report += '═══════════════════════════════════════\n\n';
        
        if (data.score) report += `📈 Overall Score: ${data.score}/100\n\n`;
        
        if (data.issues) {
            report += '🔴 Critical Issues:\n';
            data.issues.critical?.forEach(issue => {
                report += `  • ${issue}\n`;
            });
            report += '\n🟡 Medium Issues:\n';
            data.issues.medium?.forEach(issue => {
                report += `  • ${issue}\n`;
            });
        }
        
        if (data.recommendations) {
            report += '\n✅ Recommendations:\n';
            data.recommendations.forEach(rec => {
                report += `  • ${rec}\n`;
            });
        }
        
        report += '\n═══════════════════════════════════════\n';
        report += `Generated by: SEO Agent v1.0\n`;
        report += `Date: ${new Date().toLocaleString()}\n`;
        report += '═══════════════════════════════════════\n';
        
        return report;
    }

    downloadPDFReport(data) {
        const report = this.generatePDFReport(data);
        const blob = new Blob([report], { type: 'text/plain' });
        const url = URL.createObjectURL(blob);
        const a = document.createElement('a');
        a.href = url;
        a.download = `seo_report_${Date.now()}.txt`;
        a.click();
        URL.revokeObjectURL(url);
    }
}